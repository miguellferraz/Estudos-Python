x = 10
y = 3

# Operações básicas
print(x + y)
print(x - y)
print(x * y)
print(x / y)   # divisão float
print(x // y)  # divisão inteira
print(x % y)   # resto
print(x ** y)  # potência

# Funções matemáticas
print(abs(-10))   # Transforma o número negativo em positivo
print(round(3.14159, 2))  # Arredonda o número para 2 casas decimais
